package com.jatinst.money.visualizer.exception;

public class StmtParserException extends Exception {

	private static final long serialVersionUID = 8179496119925574529L;

	public StmtParserException() {
		super();
	}

	public StmtParserException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public StmtParserException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public StmtParserException(String arg0) {
		super(arg0);
	}

	public StmtParserException(Throwable arg0) {
		super(arg0);
	}

	
}
